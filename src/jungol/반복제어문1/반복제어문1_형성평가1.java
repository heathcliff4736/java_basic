package jungol.반복제어문1;

import java.util.Scanner;

public class 반복제어문1_형성평가1 {
    public class Main {
        public static void main(String[] args) {

            Scanner sc = new Scanner(System.in);
            int n = sc.nextInt();

            for (int i = 1; i <= n; i++) {
                System.out.print(i + " ");
            }
        }
    }
}
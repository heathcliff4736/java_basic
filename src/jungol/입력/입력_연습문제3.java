package jungol.입력;

public class 입력_연습문제3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.printf("%d + %d = %d\n", a, b, c);

        a = 30;
        b = 40;
        c = a + b;

        System.out.printf("%d + %d = %d", a, b, c);
    }
}

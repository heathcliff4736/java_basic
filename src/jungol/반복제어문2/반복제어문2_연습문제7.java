package jungol.반복제어문2;

public class 반복제어문2_연습문제7 {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print(i + 1 + " ");
            }
            System.out.println();
        }
        System.out.println();

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print(j + 1 + " ");
            }
            System.out.println();
        }
        System.out.println();

    }
}
